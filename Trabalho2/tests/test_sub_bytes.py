import pytest

from aes.aes import AES


def test_sub_bytes():
    a = AES()
    assert a.sub_bytes([[0x8e, 0x9f, 0xf1, 0xc6], [0x4d, 0xdc, 0xe1, 0xc7], [0xa1, 0x58, 0xd1, 0xc8], [0xbc, 0x9d, 0xc1, 0xc9]]) == [[0x19, 0xdb, 0xa1, 0xb4], [0xe3, 0x86, 0xf8, 0xc6], [0x32, 0x6a, 0x3e, 0xe8], [0x65, 0x5e, 0x78, 0xdd]]

def test_inverse_sub_bytes():
    a = AES()
    assert a.inverse_sub_bytes([[0x19, 0xdb, 0xa1, 0xb4], [0xe3, 0x86, 0xf8, 0xc6], [0x32, 0x6a, 0x3e, 0xe8], [0x65, 0x5e, 0x78, 0xdd]]) == [[0x8e, 0x9f, 0xf1, 0xc6], [0x4d, 0xdc, 0xe1, 0xc7], [0xa1, 0x58, 0xd1, 0xc8], [0xbc, 0x9d, 0xc1, 0xc9]]